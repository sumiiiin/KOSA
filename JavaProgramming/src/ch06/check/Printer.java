package ch06.check;

public class Printer {
	
	// 필드 선언
	String string;
	int Int;
	boolean Boolean;
	double Double;
	
	// 생성자 선언 (16번)
	Printer(){
		
	}
	
	Printer(String string){ // 기본 생성자
		this.string = string;
	}
	
	Printer(int Int){ // 기본 생성자
		this.Int = Int;
	}
	
	Printer(boolean Boolean){ // 기본 생성자
		this.Boolean = Boolean;
	}
	
	Printer(double Double){ // 기본 생성자
		this.Double = Double;
	}
	
	// 메소드 선언
	
	public static void println(int x) {
		System.out.println(x);
	}
	
	public static void println(String x) {
		System.out.println(x);
	}
	
	public static void println(double x) {
		System.out.println(x);
	}
	
	public static void println(boolean x) {
		System.out.println(x);
	}


}
